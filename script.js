// ===== Cart Functions =====
function addToCart(name, price, imageUrl) {
    const existingItem = cart.find(item => item.name === name);
    if(existingItem) existingItem.quantity += 1;
    else cart.push({ name, price, imageUrl, quantity: 1 });

    saveCart();
    updateCartUI();
    showCartNotification();
}

function removeFromCart(name) {
    cart = cart.filter(item => item.name !== name);
    saveCart();
    updateCartUI();
}

function updateQuantity(name, change) {
    const item = cart.find(item => item.name === name);
    if(!item) return;
    item.quantity += change;
    if(item.quantity <= 0) removeFromCart(name);
    else { saveCart(); updateCartUI(); }
}

function clearCart() {
    cart = [];
    saveCart();
    updateCartUI();
}

function saveCart() {
    localStorage.setItem('shop-cart', JSON.stringify(cart));
}

function updateCartUI() {
    const totalItems = cart.reduce((sum,item) => sum + item.quantity,0);
    cartBadge.textContent = totalItems;

    cartItems.innerHTML = cart.length === 0 ? '<div class="empty-cart">Your cart is empty</div>' : '';
    cart.forEach(item => cartItems.appendChild(createCartItem(item)));

    const total = cart.reduce((sum,item) => sum + item.price*item.quantity,0);
    cartTotal.textContent = total.toFixed(2);
}

function createCartItem(item) {
    const cartItem = document.createElement('div');
    cartItem.className = 'cart-item';
    cartItem.innerHTML = `
        <div class="cart-item-image" style="background-image:url('${item.imageUrl}')"></div>
        <div class="cart-item-info">
            <div class="cart-item-name">${item.name}</div>
            <div class="cart-item-price">$${item.price.toFixed(2)}</div>
            <div class="cart-item-controls">
                <button class="quantity-btn" onclick="updateQuantity('${item.name}', -1)">-</button>
                <span class="quantity">${item.quantity}</span>
                <button class="quantity-btn" onclick="updateQuantity('${item.name}', 1)">+</button>
            </div>
        </div>
        <button class="remove-item" onclick="removeFromCart('${item.name}')" title="Remove item">
            <i class="fas fa-times"></i>
        </button>
    `;
    return cartItem;
}

