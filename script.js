let currentSearchResults = [];

// Enhanced search function that searches in multiple fields
function performSearch(query) {
    if (!query || query.length < 1) {
        return allProducts;
    }

    const searchTerms = query.toLowerCase().trim().split(' ').filter(term => term.length > 0);
    
    return allProducts.filter(product => {
        const searchableText = [
            product.name,
            product.description,
            product.category,
            product.sku
        ].join(' ').toLowerCase();

        // Check if all search terms are found in the product
        return searchTerms.every(term => searchableText.includes(term));
    }).sort((a, b) => {
        // Sort by relevance (exact name matches first, then description matches)
        const aNameMatch = a.name.toLowerCase().includes(query.toLowerCase());
        const bNameMatch = b.name.toLowerCase().includes(query.toLowerCase());
        
        if (aNameMatch && !bNameMatch) return -1;
        if (!aNameMatch && bNameMatch) return 1;
        
        // Then sort by rating (higher rating first)
        return b.rating - a.rating;
    });
}


// Create enhanced search suggestions
function createSearchSuggestions(matchedProducts, query) {
    if (matchedProducts.length === 0) {
        return `<li class="no-results">
            <i class="fas fa-search"></i>
            <span>No products found for "${query}"</span>
            <small>Try different keywords or check spelling</small>
        </li>`;
    }

    const suggestions = matchedProducts.slice(0, 8).map(product => {
        const isExactMatch = product.name.toLowerCase().includes(query.toLowerCase());
        const matchType = isExactMatch ? 'exact' : 'related';
        
        return `<li data-id="${product.id}" class="suggestion-item ${matchType}">
            <img src="${product.image_url}" class="suggestion-image" alt="${product.name}" loading="lazy">
            <div class="suggestion-content">
                <div class="suggestion-name">${highlightSearchTerm(product.name, query)}</div>
                <div class="suggestion-details">
                    <span class="suggestion-category">${product.category}</span>
                    <span class="suggestion-price">$${product.price.toFixed(2)}</span>
                    <div class="suggestion-rating">
                        ${'★'.repeat(product.rating)}${'☆'.repeat(5 - product.rating)}
                    </div>
                </div>
            </div>
        </li>`;
    }).join('');

    // Add search statistics
    const stats = `<li class="search-stats">
        <i class="fas fa-info-circle"></i>
        <span>Found ${matchedProducts.length} product${matchedProducts.length !== 1 ? 's' : ''}</span>
        ${matchedProducts.length > 8 ? `<small>Showing top 8 results</small>` : ''}
    </li>`;

    return stats + suggestions;
}

// Highlight search terms in results
function highlightSearchTerm(text, query) {
    if (!query) return text;
    const regex = new RegExp(`(${query})`, 'gi');
    return text.replace(regex, '<mark>$1</mark>');
}

// Enhanced search event listener
searchInput.addEventListener("input", () => {
    debounce(() => {
        const query = searchInput.value.trim();
        
        if (query.length > 0) {
            // Perform search
            currentSearchResults = performSearch(query);
            
            // Update suggestions
            suggestionsBox.innerHTML = createSearchSuggestions(currentSearchResults, query);
            suggestionsBox.style.display = "block";
            
            // Add event listeners to suggestions
            suggestionsBox.querySelectorAll(".suggestion-item").forEach(li => {
                li.addEventListener("click", () => {
                    const selectedProductId = li.getAttribute('data-id');
                    const selectedProduct = allProducts.find(p => p.id === selectedProductId);
                    if (selectedProduct) {
                        searchInput.value = selectedProduct.name;
                        suggestionsBox.style.display = "none";
                        
                        // Scroll to the selected product's section
                        const sectionId = categoryToSectionId[selectedProduct.category];
                        const section = document.getElementById(sectionId);
                        if (section) {
                            section.scrollIntoView({ behavior: "smooth", block: "start" });
                            highlightProductCard(selectedProductId);
                        }
                    }
                });
            });
            
            // Show search results count
            updateSearchResultsCount(currentSearchResults.length, query);
            
        } else {
            // Clear search
            suggestionsBox.style.display = "none";
            currentSearchResults = [];
            renderProducts(allProducts);
            hideSearchResultsCount();
        }
    });
});

// Update search results count display
function updateSearchResultsCount(count, query) {
    let countElement = document.getElementById('searchResultsCount');
    if (!countElement) {
        countElement = document.createElement('div');
        countElement.id = 'searchResultsCount';
        countElement.className = 'search-results-count';
        searchInput.parentNode.appendChild(countElement);
    }
    
    countElement.innerHTML = `
        <i class="fas fa-search"></i>
        <span>${count} result${count !== 1 ? 's' : ''} for "${query}"</span>
        ${count > 0 ? '<button class="clear-search-btn" title="Clear search"><i class="fas fa-times"></i></button>' : ''}
    `;
    
    // Add clear search functionality
    const clearBtn = countElement.querySelector('.clear-search-btn');
    if (clearBtn) {
        clearBtn.addEventListener('click', () => {
            searchInput.value = '';
            suggestionsBox.style.display = 'none';
            currentSearchResults = [];
            renderProducts(allProducts);
            hideSearchResultsCount();
        });
    }
}

// Hide search results count
function hideSearchResultsCount() {
    const countElement = document.getElementById('searchResultsCount');
    if (countElement) {
        countElement.remove();
    }
}


